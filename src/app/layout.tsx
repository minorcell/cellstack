import type { Metadata } from 'next'
import './globals.css'
import { Navbar } from '@/components/Navbar'
import { Footer } from '@/components/Footer'
import DotGrid from '@/components/DotGrid'
import { siteContent } from '@/lib/site-content'

export const metadata: Metadata = {
  title: {
    default: siteContent.name,
    template: `%s | ${siteContent.name}`,
  },
  description: siteContent.description,
  alternates: {
    types: {
      'application/rss+xml': '/feed.xml',
    },
  },
  icons: {
    icon: '/logo.svg',
  },
}

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  return (
    <html lang="zh-CN" suppressHydrationWarning>
      <head>
        <script
          dangerouslySetInnerHTML={{
            __html: `
              (function() {
                const theme = localStorage.getItem('theme') || 'light'
                if (theme === 'dark' || (theme === 'auto' && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
                  document.documentElement.classList.add('dark')
                }
              })()
            `,
          }}
        />
      </head>
      <body className="min-h-screen bg-background text-foreground relative">
        <div className="pointer-events-none fixed inset-0 z-0 overflow-hidden">
          <DotGrid
            className="!p-0 h-full w-full opacity-35"
            dotSize={5}
            gap={15}
            baseColor="var(--dotgrid-base)"
            activeColor="var(--dotgrid-active)"
            proximity={120}
            speedTrigger={100}
            shockRadius={250}
            shockStrength={5}
            maxSpeed={5000}
            resistance={750}
            returnDuration={1.5}
          />
        </div>
        <div className="relative z-10 flex min-h-screen flex-col">
          <Navbar />
          <main className="flex-1 relative" data-pagefind-body>
            {children}
          </main>
          <Footer />
        </div>
      </body>
    </html>
  )
}
